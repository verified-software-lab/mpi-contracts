/* iso646.h: The ABC representation of standard C library Alternative spellings.
 * Alternative spellings <iso646.h>
 * Based on C11 Standard 7.9.
 */
#ifndef _ISO646_
#define _ISO646_

/* Defines the following eleven macros (on the left) that expand
 * to the corresponding tokens (on the right):
 */
#define and    &&
#define and_eq &=
#define bitand &
#define bitor  |
#define compl  ~
#define not    !
#define not_eq !=
#define or     ||
#define or_eq  |=
#define xor    ^
#define xor_eq ^=

#endif
